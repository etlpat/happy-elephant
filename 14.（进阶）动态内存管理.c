////////////////////  realloc()  
////// 调整动态内存空间的大小 / (官方解释：重新分配内存块)
 //
////// 头文件：<stdlib.h> 或 <malloc.h> 
////// realloc：re(前缀:重新) + allocate(分配)
//  
// 
////// 声明：void *realloc( void *memblock, size_t size );
////// [memblock] 是要调整的内存块的地址
////// [size]     是调整之后内存的新大小

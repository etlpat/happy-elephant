////// 情景 2.如果p指向的空间之后【没有足够的内存空间来追加】，则realloc函数会找新的内存区域来开辟一块满足需求的空间，
//////        并且把原来内存中的【数据拷贝到新内存中】，并【自动释放旧的内存空间】，最后返回新开辟的内存空间的【新地址】
